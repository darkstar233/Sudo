# 标准数独菜单
def standard():
    print("标准数独")
    loop_control = True
    while loop_control:

        tool.clear_screen()

        print(" 1.求解    2.游戏")
        option=input("\n请输入选项：")
        match option:
            case "1":
                print("暂时还么有")
                tool.delay(1)

            case "2":
                print("暂时还么有")
                tool.delay(1)

            case "quit" | "exit" | "退出":
                loop_control = False
                print("退出程序!!!")
                tool.delay(1)
                
            case _:
                print("输入错误，请重新输入选项!!!")
                tool.delay(1)

